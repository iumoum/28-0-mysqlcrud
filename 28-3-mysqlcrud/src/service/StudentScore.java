package service;

public class StudentScore {
	private int studentNo;
	private String studentName;
	private int StudentScoreNo;
	private int Score;
	private String scoreCheck;
	
	public int getStudentNo() {
		return studentNo;
	}
	public void setStudentNo(int studentNo) {
		this.studentNo = studentNo;
	}
	public String getStudentName() {
		return studentName;
	}
	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}
	public int getStudentScoreNo() {
		return StudentScoreNo;
	}
	public void setStudentScoreNo(int studentScoreNo) {
		StudentScoreNo = studentScoreNo;
	}
	public int getScore() {
		return Score;
	}
	public void setScore(int score) {
		Score = score;
	}
	public String getScoreCheck() {
		return scoreCheck;
	}
	public void setScoreCheck(String scoreCheck) {
		this.scoreCheck = scoreCheck;
	}
	
	
}
